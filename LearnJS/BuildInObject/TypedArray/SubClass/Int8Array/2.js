  const array3 = new Int8Array([100, 200, 300]);
  console.log(array3);

  // Int8Array(3) [ 100, -56, 44 ]

  // Int8Array 是有符号8位整数

  // 过程
  //   100  =>  0110 0100
  //   200  =>  0000 0000 1100 1000  =>  截掉 9 - 16 位，余下 1100 1000  即为 -56
  //   300  =>  0000 0001 0010 1100  =>  截掉 9 - 16 位，余下 0010 1100  即为 44