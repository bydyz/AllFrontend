<template>
  <div>
    <p class="mb-[8px]">
      <vxe-button @click="setSelectRow(0)">设置第一行选中（如果被禁用，不可选中）</vxe-button>
      <vxe-button @click="setSelectRow(1)">设置第二行选中</vxe-button>
      <vxe-button @click="clearRadioRowEvent">清除所有行选中</vxe-button>
    </p>

    <vxe-grid ref="gridRef" v-bind="gridOptions"></vxe-grid>
  </div>
</template>

<script setup>
import { ref, reactive } from 'vue'
const gridRef = ref()
const gridOptions = reactive({
  border: true,
  height: 300,
  radioConfig: {
    labelField: 'name',
    // true 可选    false 禁用
    checkMethod ({ row }) {
      return row.age > 29
    }
  },
  columns: [
    { type: 'radio', title: '请选择', width: 100 },
    { field: 'sex', title: 'Sex' },
    { field: 'age', title: 'Age' },
    { field: 'address', title: 'Address', showOverflow: true }
  ],
  data: [
    { id: 10001, name: 'Test1', role: 'Develop', sex: 'Man', age: 28, address: 'test abc' },
    { id: 10002, name: 'Test2', role: 'Test', sex: 'Women', age: 22, address: 'Guangzhou' },
    { id: 10003, name: 'Test3', role: 'PM', sex: 'Man', age: 32, address: 'Shanghai' },
    { id: 10004, name: 'Test4', role: 'Designer', sex: 'Women', age: 23, address: 'test abc' },
    { id: 10005, name: 'Test5', role: 'Develop', sex: 'Women', age: 30, address: 'Shanghai' },
    { id: 10006, name: 'Test6', role: 'Designer', sex: 'Women', age: 21, address: 'test abc' },
    { id: 10007, name: 'Test7', role: 'Test', sex: 'Man', age: 29, address: 'test abc' },
    { id: 10008, name: 'Test8', role: 'Develop', sex: 'Man', age: 35, address: 'test abc' }
  ]
})
const setSelectRow = (index) => {
  const $grid = gridRef.value
  if ($grid) {
    $grid.setRadioRow(gridOptions.data[index])
  }
}
const clearRadioRowEvent = () => {
  const $grid = gridRef.value
  if ($grid) {
    $grid.clearRadioRow()
  }
}

</script>
